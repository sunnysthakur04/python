#Comparison Operators
print("Comparison Operators")

cop1 = 2 < 4
print(cop1)

cop2 = 2 == 4
print(cop2)

cop3 = 2 > 4
print(cop3)

cop4 = 6.2 <= 6
print(cop4)

cop5 = 6.2 <= 6.2
print(cop5)

cop6 = 6.2 <= 6.20001
