#Conjuction Operators
print("Conjuction Operators")

conjop1 = 2 < 4 and 2 == 4
print(conjop1)

conjop2 = 2 > 4 or 2 < 4
print(conjop2)

conjop3 = not 6.2 <= 6
print(conjop3)

conjop4 = 3 < 4 < 5
print(conjop4)

conjop5 = 3 < 4 and 4 < 5
